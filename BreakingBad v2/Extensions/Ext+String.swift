import Foundation

extension String {
    
    enum color: String {
        case gray100 = "gray100"
        case gray90 = "gray90"
        case gray80 = "gray80"
        case gray70 = "gray70"
        case blue = "blue"
        case green = "green"
        case yellow = "yellow"
    }
    enum icons: String {
        case chevronRight = "chevronRight"
    }
    enum placeHolders: String {
        case quote = "Please enter a quote"
        case memberName = "Member Name"
    }
}
